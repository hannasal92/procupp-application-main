import Selection from "@/components/Products/Selection/Selection";
import axios from "axios";
import type { GetStaticProps } from "next";
import Head from "next/head";

const ProductsPage = () => {
  return (
    <>
      <Head>
        <title>ProCupp - Products</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Selection />
    </>
  );
};

export default ProductsPage;

export const getStaticProps: GetStaticProps = async (context) => {
  let messages = await axios.get(
    `${process.env.NEXT_PUBLIC_PAYLOAD_SERVER}/api/languages?where[locale][equals]=${context.locale}`
  );

  return {
    props: {
      messages: messages.data.docs[0].data,
      // messages: (await import(`../messages/${context.locale}.json`)).default,
    },
    revalidate: 60,
  };
};
